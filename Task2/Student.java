
public class Student {

    int Id;
    String name;
    int marks;

    public Student(int Id, int marks, String name) {
        this.Id = Id;
        this.marks = marks;
        this.name = name;
    }

    public int getId() {
        return Id;
    }

    public void setId(int id) {
        Id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getMarks() {
        return marks;
    }

    public void setMarks(int marks) {
        this.marks = marks;
    }

}
